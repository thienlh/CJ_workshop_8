    private void btnSave2fileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave2fileActionPerformed
        try {
            FileWriter fw = new FileWriter(fileName);
            PrintWriter pw = new PrintWriter(fw);
            int n = tblEmployee.getRowCount();
            for (int i=0; i<n; i++) {
                Employee row = model.getData().elementAt(i);
                String gender = row.gender == true ? "MALE" : "FEMALE";
                String detail = row.getId() + ";" + row.getName() + ";" + row.getAddress()+ ";" + gender + ";" + row.getSalary();
                pw.println(detail);
            }
            pw.close();
            fw.close();
            JOptionPane.showMessageDialog(null, "Data saved!");
        } catch (Exception e) {
            e.getStackTrace();
        }
    }//GEN-LAST:event_btnSave2fileActionPerformed

