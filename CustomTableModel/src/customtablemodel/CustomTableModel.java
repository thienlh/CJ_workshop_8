    private void tblEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeMouseClicked
        //  Show the current employee detail
        int pos = tblEmployee.getSelectedRow();
        Employee currentEmp = model.getData().get(pos);
        this.txtEmpID.setText(currentEmp.getId());
        this.txtEmpName.setText(currentEmp.getName());
        this.txtEmpAdrs.setText(currentEmp.getAddress());
        this.rbtnMale.setSelected(currentEmp.isGender());
        this.rbtnFemale.setSelected(!currentEmp.isGender());
        this.txtEmpSalary.setText(currentEmp.getSalary() + "");   //  What "" use for???
        this.txtEmpID.setEditable(true);
        this.addNew = false;
    }//GEN-LAST:event_tblEmployeeMouseClicked

