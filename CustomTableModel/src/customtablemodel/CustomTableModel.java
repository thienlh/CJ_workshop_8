    private void loadData() {
        try {
            FileReader fr = new FileReader(fileName);
            BufferedReader br = new BufferedReader(fr);
            String detail = "";
            while ((detail = br.readLine()) != null) {
                StringTokenizer stk = new StringTokenizer(detail, ";");
                String id = stk.nextToken().trim();
                String name = stk.nextToken().trim();
                String address = stk.nextToken().trim();
                String genderStr = stk.nextToken().trim();
                boolean gender = (genderStr.equalsIgnoreCase("MALE") == true);
                int salary = Integer.parseInt(stk.nextToken());
                model.getData().add(new Employee(id, name, address, gender, salary));
            }
            br.close();
            fr.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

