    private void btnEmpSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpSaveActionPerformed
        if (!valid()) {
            return;
        }
        String id = txtEmpID.getText();
        String name = txtEmpName.getText();
        String address = txtEmpAdrs.getText();
        boolean gender = rbtnFemale.isSelected();
        int salary = Integer.parseInt(txtEmpSalary.getText());
        Employee newEmp = new Employee(id, name, address, gender, salary);
        if (addNew) {
            model.getData().add(newEmp);
        } else {
            //  Replace Employee detail
            int pos = tblEmployee.getSelectedRow();
            model.getData().set(pos, newEmp);
        }
        tblEmployee.updateUI();
    }//GEN-LAST:event_btnEmpSaveActionPerformed

    private void btnEmpRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpRemoveActionPerformed
        int pos = tblEmployee.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure to remove this employee?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            Employee emp = model.getData().elementAt(pos);
            model.getData().remove(emp);
        }
        tblEmployee.updateUI();
    }//GEN-LAST:event_btnEmpRemoveActionPerformed

